      let params = {
        app_id: 2155983479,
        time_stamp: this.getTimeStamp(),
      };
      this.axios
        .get(baseUrl + requestUrl, { params })
        .then((res) => {
          console.log(res);
        })
        .catch((err) => {
          console.log(err);
        });
    },
  },
};
